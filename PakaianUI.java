    private void addPakaian(String merk, double harga, PakaianListener.TambahPakaian listener) {
        if (jumlahPakaian < 5) {
            boolean ada = false;
            for (int i = 0; i < jumlahPakaian; i++) {
                if (arrayPakaian[i].getMerk().equalsIgnoreCase(merk)) {
                    ada = true;
                    break;
                }
            }
            if (ada == true) {
                listener.merkSudahAda("Merk sudah ada");
            } else {
                arrayPakaian[jumlahPakaian] = new cPakaian(merk, harga);
                listDataPakaian[0][0] = arrayPakaian[jumlahPakaian].getMerk();
                listDataPakaian[0][1] = String.valueOf(arrayPakaian[jumlahPakaian].getHarga());
                addPakaianToTable(listDataPakaian[0]);
                jumlahPakaian++;
            }
        } else {
            listener.kapasitasPenuh("Kapasitas penuh!");
        }
    }

