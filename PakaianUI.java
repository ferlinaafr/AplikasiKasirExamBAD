    private void jButtonUbahPakaianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUbahPakaianActionPerformed
        //asumsinya yg berubah adalah data harga saja
        boolean ketemu = false;
        int indeks = 0;
        String id = JOptionPane.showInputDialog(this, "Masukkan Merk");
        //cek cari dulu ID di Array Pelanggan
        for (int i = 0; i < jumlahPakaian; i++) {
            indeks = i;
            if (arrayPakaian[i].getMerk().equalsIgnoreCase(id)) {
                ketemu = true;
                break;
            }
        }
        if (ketemu == true) {
            String h = JOptionPane.showInputDialog(this, "Masukkan Harga Baru");
            int j = JOptionPane.showConfirmDialog(this, "Yakin diubah");
            if (j == 0) {
                arrayPakaian[indeks].setHarga(Double.parseDouble(h));
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                //update sel data pada baris indeks, kolom ke-2, dengan data a1
                model.setValueAt(h, indeks, 1);
                JOptionPane.showMessageDialog(this, "Berhasil diperbarui...");
            } else {
                JOptionPane.showMessageDialog(this, "Batal update!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Merk tidak ada!");
        }
    }//GEN-LAST:event_jButtonUbahPakaianActionPerformed

    private void jButtonRemovePakaianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonRemovePakaianActionPerformed
        //cai dulu merk di array pelanggan
        boolean ketemu = false;
        int indeks = 0;
        String id = JOptionPane.showInputDialog(this, "Masukkan Merk");
        //cek cari dulu ID di Array Pelanggan
        for (int i = 0; i < jumlahPakaian; i++) {
            indeks = i;
            if (arrayPakaian[i].getMerk().equalsIgnoreCase(id)) {
                ketemu = true;
                break;
            }
        }
        if (ketemu == true) {
            int j = JOptionPane.showConfirmDialog(this, "Yakin dihapus");
            if (j == 0) {
                for (int i = 0; i < jumlahPakaian; i++) {
                    if (i == jumlahPakaian - 1) {
                        arrayPakaian[i] = null;
                    } else {
                        arrayPakaian[i] = arrayPakaian[i + 1];
                    }
                }
                jumlahPakaian--;
                //update isi tabel
                DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
                model.removeRow(indeks);
                JOptionPane.showMessageDialog(this, "Berhasil dihapus...");
            } else {
                JOptionPane.showMessageDialog(this, "Batal hapus!");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Merk tidak ada!");
        }
    }//GEN-LAST:event_jButtonRemovePakaianActionPerformed

